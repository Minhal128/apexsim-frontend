export function getAppLanguage(): AppLanguageCode {
  if (typeof window === "undefined") return "Eng";
  const stored = localStorage.getItem(APP_LANGUAGE_STORAGE_KEY);
  return stored === "Esp" ? "Esp" : "Eng";
}

export function t(key: string, lang: AppLanguageCode): string {
  return messages[key]?.[lang] ?? messages[key]?.Eng ?? key;
}
